export function translate(key: TranslationKey, language: LanguageCode) {
  return translations[language][key] || translations.en[key];
}

export function translateVisibleText(value: string, language: LanguageCode) {
  const match = (Object.keys(en) as TranslationKey[]).find((key) =>
    languageCodes.some((code) => translations[code][key] === value),
  );
  if (match) return translations[language][match];
  return legacyTranslations[language][value] || value;
}

